    private void toggleRecording() {
        recording = !recording;
        if (recording) {
            timeRodDequencStartedMs = System.currentTimeMillis();
            rodSequence.clear();
        } else {
            firePropertyChange(GoingFishing.EVENT_ROD_SEQUENCE, null, rodSequence);
        }
    }

